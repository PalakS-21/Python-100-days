
num1 = input("Enter first number : ")
num2 = input("Enter second number : ")

#Addition
a = int(num1) + int(num2)
print("Addition of " +num1+ " and " +num2+ " is : ", a)

#Subtraction
b = int(num1) - int(num2)
print("Subtraction of " +num1+ " and " +num2+ " is : ", b)

#Multiplication
c = int(num1) * int(num2)
print("Multiplication of " +num1+ " and " +num2+ " is : ", c)

#Division
d = int(num1) / int(num2)
print("Division of " +num1+ " and " +num2+ " is : ", d)