
#Logical Operators : and, or, not

#and operator : both condition must be true.
print(5 > 3 and 8 > 5) #true
print(5 > 7 and 4 > 3) #false

# or operator : retuirns true if atleast one condition is true.
print(5 > 7 or 4 > 3) #true
print(15 < 7 or 6 > 8) #false

# not operator : flips the result, true to false and false to true.
print(not(5>3)) #false
print(not(3>5)) #true
print(0 and 5)