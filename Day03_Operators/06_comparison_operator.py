
#Comparison Operators : ==, !=, >, <, >=, <=
# these compare values, and gives output as Boolean (True/False).

# (==) -> checks if the vlaues are equal or not
print(5==3) #false
print(5==5) #true

# (!=) -> not equals to, checks if two values are not equal.
print(2!=3) #true
print(2!=2) #false

# >, < : greater than, less than
print(2>3) #True/False
print(2<3) #True/False

# >=, <= : greter or equal, less or equal
print(2<=8) #true
print(2>=6) #false